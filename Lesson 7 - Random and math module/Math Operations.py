import math
print('The floor and ceiling values of 28.56 are' + str(math.ceil(28.56)) + ', '
      + str(math.floor(28.56)))

x = 10
y = -15
print("The value of x after copying from the y sign is: " + str(math.copysign(x, y)))

print("Absolute value of -96 and 54 are: "+ str(math.fabs(-96))
      + ', ' + str(math.fabs(54)))

print("The GCD of 24 and 56 is: " + str(math.gcd(24, 56)))